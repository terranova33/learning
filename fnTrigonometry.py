
# trigonometric functions

import math

# sin(x) - Return the sine of x, in radians
print("sin(3): ", math.sin(3))

# cos(x) - Return the cosine of x, in radians
print("cos(3): ", math.cos(3))

# tan(x) - Return the tan of x, in radians
print("tan(pi/2): ", math.tan(math.pi/2))



# Inverse Trigonometry
# asin(x) - Return the arc sine of x, in radians
print("asin(0.64): ", math.asin(0.64))

# acos(x) - Return the arc cosine of x, in radians
print("acos(0.64): ", math.acos(0.64))

# atan(x) - Return the arc tangent of x, in radians
print("atan(0): ", math.atan(0))

# atan2(x) - Return the atan(x/y), in radians
#print("atan2(5,5): ", math.atan2(5,5)

# miscellaneous functions

print("hypot(x,y): ", math.hypot(10,30))   # return the Euclidean norm sqrt

print("degrees(x): ", math.degrees(2))   # Converts angle x from radians to degrees

print("radians(x): ", math.degrees(90))  # Converts angle x from degrees to radians
